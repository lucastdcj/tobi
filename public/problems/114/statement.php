<p align="justify">

<p>Vó Vitória mantém, desde o nascimento dos netos Jo&#227;ozinho e Zezinho, um ritual que faz a alegria dos meninos. Ela guarda todas as moedas recebidas como troco em dois pequenos cofrinhos,

um para cada neto. Quando um dos cofrinhos &#64257;ca cheio, ela chama os dois netos para um alegre

almoço, ao &#64257;nal do qual entrega aos garotos as moedas guardadas nos cofrinhos de cada um.</p>



<p>Ela sempre foi muito zelosa quanto &#224; distribuiç&#227;o igualitária do troco arrecadado. Quando, por

força do valor das moedas, ela n&#227;o consegue depositar a mesma quantia nos dois cofrinhos, ela

memoriza a diferença de forma a compensá-la no próximo depósito.</p>

<h3>Tarefa</h3>

<p>Vó Vitória está &#64257;cando velha e tem medo que deslizes de memória a façam cometer injustiças

com os netos, deixando de compensar as diferenças entre os cofrinhos. Sua tarefa é ajudar Vó

Vitória, escrevendo um programa de computador que indique as diferenças entre os depósitos, de

forma que ela n&#227;o tenha que preocupar-se em memorizá-las.

</p>

<h3>Entrada</h3>

<p>A entrada é composta de vários conjuntos de teste. A primeira linha de um conjunto de teste contém um número inteiro N, que indica o número de depósitos nos cofrinhos. As N linhas seguintes

descrevem cada uma um depósito nos cofrinhos; o depósito é indicado por dois valores inteiros J

e Z, separados por um espaço em branco, representando respectivamente os valores, em centavos,

depositados nos cofres de Jo&#227;ozinho e Zezinho. O &#64257;nal da entrada é indicado por <code>N = 0</code>.</p>



<h3>Saída</h3>

<p>Para cada conjunto de teste da entrada seu programa deve produzir um conjunto de linhas na

saída. A primeira linha deve conter um identi&#64257;cador do conjunto de teste, no formato &#8220;Teste n&#8221;,

onde n é numerado seqüencialmente a partir de 1. A seguir seu programa deve escrever uma linha

para cada depósito do conjunto de testes. Cada linha deve conter um inteiro que representa a diferença (em centavos) entre o valor depositado nos cofrinhos do Jo&#227;ozinho e do Zezinho. Deixe uma linha em branco ao &#64257;nal de cada conjunto de teste. A gra&#64257;a mostrada no Exemplo de Saída,

abaixo, deve ser seguida rigorosamente.</p>

<h3>Exemplo</h3>

<pre>

<b>Entrada:</b>

3

