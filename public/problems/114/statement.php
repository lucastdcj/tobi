17 1

0



<b>Saída:</b>



Teste 1

-5

0

0



Teste 2

-5

7

-13

3



</pre>



<h3>Restriç&#245;es</h3>

<p><code>0 <= N <= 100</code> (N = 0 apenas para indicar o &#64257;m da entrada)<br>

<code>0 <= J <= 100</code> (valor de cada depósito no cofre de Jo&#227;ozinho)<br>

<code>0 <= Z <= 100</code> (valor de cada depósito no cofre de Zezinho)</p>



