<p>Joãozinho adora quebra-cabeças, essa é sua brincadeira favorita. O grande problema, porém, é que às vezes
o jogo vem com uma peça faltando. Isso irrita bastante o pobre menino, que tem de descobrir qual peça está
faltando e solicitar uma peça de reposição ao fabricante do jogo. Sabendo que o quebra-cabeças tem <i>N</i> peças,
numeradas de 1 a <i>N</i> e que exatamente uma está faltando, ajude Joãozinho a saber qual peça ele tem de pedir.</p>
<h3>Tarefa</h3>
<p>Escreva um programa que, dado um inteiro <i>N</i> e <i>N</i> - 1 inteiros numerados de 1 a <i>N</i>, descubra qual inteiro está
faltando.</p>
<h3>Entrada</h3>
<p>A entrada contém um único conjunto de testes, que deve ser lido do <i>dispositivo de entrada padrão</i> (normalmente
o teclado). A entrada contém 2 linhas. A primeira linha contém um inteiro <i>N</i> (2 &le; <i>N</i> &le; 1.000). A segunda
linha contém <i>N</i> - 1 inteiros numerados de 1 a <i>N</i> (sem repetições).</p>

<h3>Sa&iacute;da</h3>
<p>Seu programa deve imprimir, na <i>saída padrão</i>, uma única linha, contendo o número que está faltando na
sequência dada.</p>

