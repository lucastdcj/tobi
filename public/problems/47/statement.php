<p align="justify">

A Copa do Mundo de 2010 será realizada na Africa do Sul. Bolas de futebol são muito fáceis de transportar, já
que elas saem das fábricas vazias e só são enchidas somente pelas lojas ou pelos consumidores finais.
Infelizmente o mesmo não pode ser dito das bolas de boliche. Como elas são completamente sólidas, elas só
podem ser transportadas embaladas uma a uma, em caixas separadas.
<p>
A SBC -- Só Boliche Cascavel -- é uma fábrica de bolas de boliche que trabalha somente através de encomendas
e envia todas as bolas por SEDEX. Como as bolas têm tamanhos diferentes, a SBC tem vários tamanhos de
caixas diferentes para transportá-las.</p>
<p>
Escreva um programa que, dado o diâmetro de uma bola e as 3 dimens?oes de uma caixa (altura, largura e
profundidade), diz se a bola de boliche cabe dentro da caixa ou não.</p>

<h3>Entrada</h3>
<p>
A primeira linha da entrada contém um inteiro N que indica o diâmetro da bola de boliche. A
segunda linha da entrada contém 3 números inteiros separados por um espaço cada: a altura A,
seguida da largura L e da profundidade P.</p>

<h3>Saída</h3>
<p>
Seu programa deve imprimir uma única linha, contendo a letra 'S' caso a bola de boliche caiba dentro da caixa
ou 'N' caso contrário.</p>
<p>
</p>

<h3>Restrições</h3>
<ul>
<li>1 &le; N &le; 10<sup>4</sup></li>
<li>1 &le; A &le; 10<sup>4</sup></li>
<li>1 &le; L &le; 10<sup>4</sup></li>
<li>1 &le; P &le; 10<sup>4</sup></li>
</ul>

