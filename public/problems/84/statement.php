Ao invés de discar uma letra, disca-se a tecla que contém aquela
letra. Por exemplo, se você quiser discar o número 0800-FALE-SBC, você
na realidade discaria 0800-3253-722.

A sua avó tem reclamado de problemas de vista -- em particular, ela
não consegue mais enxergar as letrinhas nas teclas do telefone, e por
isso queria que você fizesse um programa que convertesse as letras em
um número de telefone para dígitos.</p>

<h2>Entrada</h2>
<p>
A entrada contém um único conjunto de testes, que deve ser lido do
<i>dispositivo de entrada padrão</i>. A
entrada é composta de apenas uma linha, contendo o número de telefone que
deve ser traduzido. O número de telefone contém entre 1 e 15
caracteres, que podem ser 
letras de A a Y e hífens (-).</p>

<h2>Saída</h2>
<p>
Seu programa deve imprimir na <i>saída padrão</i> 
uma única linha,
contendo o número de telefone com as letras convertidas para dígitos.
Hífens no número telefone devem ser mantidos no número de
telefone de saída.
</p>

<h2>Restrições</h2>
<ul>
<li>O número de telefone contém entre 1 e 15
caracteres, que podem ser 
letras de A a Y e hífens (-).</li>
</ul>

