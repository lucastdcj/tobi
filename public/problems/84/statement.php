<p>
As primeiras redes públicas de telefonia foram construídas pela AT&T; no
começo do século XX. Elas permitiam que seus assinantes conversassem
com a ajuda de uma <i>telefonista</i>, que conectava as linhas dos
assinantes com um cabo especial.

Essas redes evoluíram muito desde então, com a ajuda de vários avanços
tecnológicos. Hoje em dia, essas redes atendem centenas de milhões de
assinantes; ao invés de falar diretamente com uma telefonista, você
pode simplesmente discar o número da pessoa desejada no telefone.

Cada assinante recebe um número de telefone -- por exemplo,
55-98-234-5678. Qualquer pessoa que discar esse número consegue então
falar com a pessoa do outro lado da linha. Os hífens no número de
telefone são só para facilitar a leitura, e não são discados no
telefone.

Para que fique mais fácil de se lembrar de um número de telefone,
muitas companhias divulgam números que contém letras no lugar de
dígitos. Para convertê-los de volta para dígitos, a maioria dos
telefones tem letras nas suas teclas:

