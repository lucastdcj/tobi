<p>A Federa&ccedil;&atilde;o de Corridas de Charrete (FCC) organiza todo ano a Subida Brigite Cardoso (SBC), disputada nas ladeiras de paralelep&iacute;pedo de Ouro Preto. A corrida &eacute; uma das mais tradicionais do esporte, completando 100 anos em 2013. Para comemorar o centen&aacute;rio, a FCC pretende integrar dispostivos GPS &agrave;s charretes, permitindo aos espectadores desfrutarem de dados de telemetria em tempo real.</p>

<p>No mesmo vi&eacute;s de inova&ccedil;&atilde;o tecnol&oacute;gica, a FCC transmitir&aacute; a SBC via sat&eacute;lite para todo o planeta, e quer integrar a telemetria na transmiss&atilde;o, indicando qual seria o vencedor da corrida se as charretes mantivessem suas velocidades at&eacute; o final da corrida; ela pediu que voc&ecirc; escrevesse um programa que, dados as dist&acirc;ncias at&eacute; a linha de chegada, as velocidades e os n&uacute;meros das duas charretes que lideram a corrida, determina quem seria o vencedor da corrida (voc&ecirc; pode supor que as charretes n&atilde;o cruzam a linha de chegada simultaneamente).</p>

<h3>Entrada</h3>

<p>A entrada consiste de duas linhas; cada linha descreve uma das charretes que lidera a corrida. A descri&ccedil;&atilde;o de uma charrete consiste de tr&ecirc;s inteiros N, D e V indicando, respectivamente, o n&uacute;mero da charrete, a sua dist&acirc;ncia &agrave; linha de chegada em metros, e a sua velocidade, em quil&ocirc;metros por hora. Os n&uacute;meros das duas charretes s&atilde;o distintos.</p>

<h3>Sa&iacute;da</h3>

<p>Imprima uma &uacute;nica linha, contendo um &uacute;nico n&uacute;mero inteiro, indicando o n&uacute;mero da charrete que seria vencedora, conforme descrito acima.</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>1 &le; <em>N</em> &le; 99 </li>

<li>0 &lt; <em>D</em> &le; 1000 </li>

<li>0 &lt; <em>V</em> &le; 50 </li>

</ul>

<h3>Exemplos</h3>

<pre><strong>Entrada</strong>

45 900 40

17 300 20



<strong>Sa&iacute;da</strong>

17



<strong>Entrada</strong>

1 1000 100

2 1000 99



<strong>Sa&iacute;da</strong>

1

</pre>

