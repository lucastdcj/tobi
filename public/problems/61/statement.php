<h3>Tarefa</h3>

<p>Escreva um programa que, dado o número total de árvores que devem ser plantadas, de quantas maneiras
diferentes elas podem ser dispostas.</p>

<h3>Entrada</h3>

<p>A única linha da entrada contém um único inteiro <i>N</i>, que indica o número total de árvores que devem ser
plantadas (1 &le; <i>N</i> &le; 10<sup>9</sup>).</p>

<h3>Saída</h3>
<p>Seu programa deve imprimir uma única linha, contendo um único inteiro, indicando o número de arranjos
distintos que podem ser feitos para o reflorestamento.
</p>

<h3>Informações sobre a pontuação</h3>
<ul>
  <li>Em um conjunto de casos de teste que totaliza 40 pontos, <i>N</i> &le; 10.000.</li>
</ul>

