<p align="justify">

<p>O desmatamento é um dos maiores problemas enfrentados pelo Brasil hoje; estima-se que mais de 10 mil km<sup>2</sup> de
vegetação sejam desflorestados todo ano. Além de destruir os habitats de várias espécies em risco de extinção, o
desmatamento promove a emissão de gás carbônico, principal responsável pelo efeito estufa e pelo aquecimento
global.</p>
<p>A Fundação de Conservação dos Carvalhos (FCC) tenta combater esta tendência, promovendo o reflorestamento
das regiões desmatadas. Para isso, eles pretendem plantar carvalhos formando um quadriculado (um carvalho
em cada vértice); no centro de cada quadrado formado por eles, a FCC também plantará um eucalipto. Para
preservar a biodiversidade da área plantada, pelo menos uma árvore de cada espécie deve ser plantada durante
o reflorestamento.</p>
<p>Por exemplo, se a FCC quiser plantar 23 árvores, ela poderá fazê-lo de duas maneiras: ou formando um retângulo
3 <i>×</i> 5 com os carvalhos, como na figura (a), ou formando um retângulo 2 <i>×</i> 8, como na figura (b).</p>
<p>Considere que, para os propósitos deste problema, um retângulo <i>x × y</i> é equivalente a um retângulo <i>y × x</i>.</p>


