<p>Vov&oacute; Vit&oacute;ria possui muitos netinhos; como toda boa av&oacute;, ela se preocupa constantemente com a sa&uacute;de de seus netos, e quer garantir que eles estejam sempre bem agasalhados o tempo todo.</p>

<p>Vov&oacute; Vit&oacute;ria disp&otilde;e de um saco com v&aacute;rios retalhos quadrados de mesmo tamanho, em tr&ecirc;s cores diferentes, e quer us&aacute;-los para costurar cachec&oacute;is para seus netos. Ela quer que cada cachecol tenha tr&ecirc;s retalhos de largura por <em>N</em> de comprimento e, al&eacute;m disso, retalhos adjacentes devem ter cores diferentes. Por exemplo, a figura abaixo mostra tr&ecirc;s cachec&oacute;is que Vov&oacute; Vit&oacute;ria pode costurar.</p>

<img src="http://www.spoj.com/content/marcoskwkm:cache13.jpg" alt="" />

<p>Vov&oacute; Vit&oacute;ria tem muitos netos, e quer fazer um cachecol diferente para cada um deles, mas ela n&atilde;o sabe de quantas formas ela pode arrumar os retalhos para formar cachec&oacute;is diferentes. Por isso, ela pediu para voc&ecirc; escrever um programa que determina quantos cachec&oacute;is diferentes ela pode costurar.</p>

<h3>Entrada</h3>

<p>A entrada consiste de uma &uacute;nica linha contendo um &uacute;nico inteiro <em>N</em>, indicando o n&uacute;mero de retalhos no comprimento do cachecol.</p>

<h3>Sa&iacute;da</h3>

<p>Imprima uma &uacute;nica linha contendo um &uacute;nico n&uacute;mero inteiro, indicando o n&uacute;mero de cachec&oacute;is distintos que a Vov&oacute; Vit&oacute;ria pode costurar. Como este n&uacute;mero pode ser muito grande, imprima o resto que este n&uacute;mero deixa quando dividido por 1.000.000.007 (10<sup>9</sup> + 7).</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>1 &le; <em>N</em> &le; 10<sup>18</sup> </li>

</ul>

