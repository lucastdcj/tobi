<p>
ldo é um garoto muito esperto que adora promoções e sorteios. Como
já participou de muitas promoções da forma `<i>para participar, envie  n  rótulos de produtos
...</i>', Aldo tem o costume de guardar o rótulo de todos os produtos que compra. Dessa
forma, sempre que uma empresa faz uma promoção ele já tem um monte de
rótulos para mandar.</p>
<p>
A SBC (Super Balas e Caramelos) está fazendo uma nova
promoção, e, como era de se esperar, Aldo quer participar.  
Para participar da promoção é preciso enviar um envelope contendo um
rótulo de cada tipo de bala que a SBC produz. Por exemplo, se a SBC produz 3 tipos de
balas, A, B, C, e uma pessoa tem 3 rótulos de A, 3 de B e 2 de C, ela
pode enviar no máximo 2 envelopes, já que falta um rótulo de C para
compor o terceiro envelope. Não há limite para o número de envelopes
que uma pessoa pode enviar.</p> 
<p>
Balas são a segunda coisa de que Aldo mais gosta (a primeira
como você sabe são promoções). Por causa disso a quantidade de rótulos
de balas que ele tem é muito grande, e ele não está conseguindo determinar
a quantidade máxima de envelopes que ele pode enviar.</p>
<p>
Como você é o melhor amigo de Aldo ele pediu sua ajuda para
fazer o cálculo, de modo que ele compre o número exato de envelopes.
Você deve escrever um programa que, a partir da lista de rótulos de Aldo,
calcula o número máximo de envelopes válidos que ele pode enviar.</p>

<h3>Entrada</h3>
<p>
A primeira linha da entrada contém um número inteiro  N representando o número de tipos diferentes de balas que
a SBC produz. A segunda linha da entrada contém  N  números inteiros, 
cada um representando uma quantidade de rótulos de balas que Aldo tem. 
O primeiro número nessa linha
representa a quantidade de rótulos do tipo 1 que Aldo possui, o segundo número
representa a quantidade de rótulos do tipo 2, e assim por diante, até
o último número, que representa a quantidade de rótulos de tipo  N  que Aldo possui.</p>

<h3>Saída</h3>
<p>
Seu programa deve imprimir na <i>saída padrão</i> 
</p>

<h3>Restrições</h3>
<ul>
<li>1 &le; N &le; 1000</li>
</ul>

<h3>Informações sobre a pontuação </h3>

<ul>

<li>Em um conjunto de casos de teste que totaliza 30 pontos,  N &leq;
  10.</li>

<li>Em um conjunto de casos de teste que totaliza 80 pontos,  N &leq;
  100.</li>

</ul>

