<h3>Tarefa</h3>
<p>Dados os resultados dos quinze jogos da Fase Final, escreva um programa que determine a equipe campeã.</p>

<h3>Entrada</h3>
<p>A entrada é composta de quinze linhas, cada uma contendo o resultado de um jogo. A primeira linha contém
o resultado do jogo de número 1, a segunda linha o resultado do jogo de número 2, e assim por diante. O
resultado de um jogo é representado por dois números inteiros M e N separados por um espaço em branco,
indicando respectivamente o número de gols da equipe representada à esquerda e à direita na tabela de jogos
(0 &le; M &le; 20, 0 &le; N &le; 20 e M 6= N).</p>

<h3>Sa&iacute;da</h3>
<p>Seu programa deve imprimir uma única linha, contendo a letra identificadora da equipe campeã.</p>

