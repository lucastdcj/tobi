um título, tornando-se hexacampeã.</p>
<p>Na Copa do Mundo, depois de uma fase de grupos, dezesseis equipes disputam a Fase Final, composta de quinze
jogos eliminatórios. A figura abaixo mostra a tabela de jogos da Fase Final:</p>
