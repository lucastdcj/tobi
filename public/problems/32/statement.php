<p>Euclides &eacute; um garoto que gosta muito de colecionar selos. No seu anivers&aacute;rio, seus pais o presentearam com
<i>N</i> selos, todos em formato de quadrados com 1 cm de lado. Euclides gostaria de guardar todos os <i>N</i> selos
que ganhou colando-os numa p&aacute;gina de papel em branco. Ao decidir por guard&aacute;-los assim, no entanto, ele logo
percebeu que a &uacute;nica forma que lhe agradava de posicionar os selos na p&aacute;gina era a forma de um ret&acirc;ngulo
completamente coberto pelos mesmos, sem sobreposi&ccedil;&atilde;o.</p>

<p>Ele percebeu tamb&eacute;m que, independente do n&uacute;mero de selos obtido, colocar todos os selos numa &uacute;nica linha ou
todos os selos numa &uacute;nica coluna &eacute; uma configura&ccedil;&atilde;o v&aacute;lida. Como essa maneira usa a p&aacute;gina do caderno de
um jeito muito ineficiente, Euclides gostaria de saber se existe algum modo de dispor os <i>N</i> selos num ret&acirc;ngulo
que tenha mais de uma linha e mais de uma coluna tal que todas as linhas e colunas sejam completamente
ocupadas por selos (isto &eacute;, tal que n&atilde;o existam posi&ccedil;&otilde;es sem selos no interior do ret&acirc;ngulo).</p>


<h2>Entrada</h2>

<p>A entrada cont&eacute;m uma &uacute;nica linha com um &uacute;nico inteiro <i>N</i>, o n&uacute;mero de selos que Euclides ganhou.</p>


<h2>Sa&iacute;da</h2>
<p>A sa&iacute;da deve conter uma linha com um &uacute;nico caracter, que deve ser 'S' se for poss&iacute;vel organizar os selos em um
ret&acirc;ngulo com mais que uma linha e mais que uma coluna ou '<i>N</i>' caso n&atilde;o seja poss&iacute;vel.</p>

<h2>Restri&ccedil;&otilde;es</h2>

  <ul>
    <li>1 &le; <i>N</i> &le; 10 000 000 000.</li>
  </ul>

<h2>Informa&ccedil;&otilde;es sobre a pontua&ccedil;&atilde;o</h2>

  <ul>
    <li>Em um conjunto de casos de teste que totaliza 70 pontos, <i>N</i> &le; 1 000 000.</li>
  </ul>



