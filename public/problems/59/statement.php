<p align="justify">

<p>O cometa Halley é um dos cometas de menor período do Sistema Solar, completando uma volta em torno do
Sol a cada 76 anos; na última ocasião em que ele tornou-se visível do planeta Terra, em 1986, várias agências
espaciais enviaram sondas para coletar amostras de sua cauda e assim confirmar teorias sobre suas composições
químicas.</p>

<h3>Tarefa</h3>
<p>Escreva um programa que, dado o ano atual, determina qual o próximo ano em que o cometa Halley será visível
novamente do planeta Terra. Se o ano atual é um ano de passagem do cometa, considere que o cometa já passou
nesse ano (ou seja, considere sempre o próximo ano de passagem, não considerando o ano atual).</p>

<h3>Entrada</h3>
<p>A única linha da entrada contém um único inteiro <i>A</i> (2010 &le; <i>A</i> &le; 10<sup>4</sup>), indicando o ano atual.</p>

<h3>Sa&iacute;da</h3>
<p>Seu programa deve imprimir uma única linha, contendo um número inteiro, indicando o próximo ano em que o
cometa Halley será visível novamente do planeta Terra.</p>

<h3>Informa&ccedil;&otilde;es sobre a pontua&ccedil;&atilde;o</h3>
<ul>
  <li>Em um conjunto de casos de teste que totaliza 20 pontos, <i>A</i> &le; 2400.</li> 
</ul>

