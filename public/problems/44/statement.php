<p>
O Shopping Boas Compras - SBC, através de sua política ambiental, está preocupado com o consumo de energia
e, resolveu trocar todas as escadas rolantes por modelos mais modernos, que se desligam caso ningúem esteja
utilizando, poupando energia.</p>
<p>
A nova escada rolante possui um sensor no início. Toda vez que ela está vazia e algúem passa pelo sensor, a
escada começa a funcionar, parando de funcionar novamente após 10 segundos se ningúem mais passar pelo
sensor. Estes 10 segundos representam o tempo suficiente para levar algúem de um nível ao outro.
Preocupados em saber exatamente quanto de energia o shopping está economizando, o gerente pediu sua ajuda.
Como eles sabem qual era o consumo da escada rolante antiga, eles te pediram para calcular o tempo que a
nova escada ficou funcionando.</p>
<p>
Dados os instantes, em segundos, em que passaram pessoas pela escada rolante, você deve calcular quantos
segundos ela ficou ligada.</p>

<h3>Entrada</h3>
<p>
A primeira linha da entrada contém um inteiro N que indica o número de pessoas que o sensor detectou. 
As N linhas seguintes representam o instante em que a i-ésima pessoa passou pelo sensor e
contém um inteiro T. Os tempos estão em ordem crescente, sem repetições.</p>

<h3>Saída</h3>
<p>
Seu programa deve imprimir uma única linha, contendo o tempo que a escada rolante ficou ligada.
</p>

<h3>Restrições</h3>
<ul>
<li>1 &le; N &le; 1000</li>
<li>0 &le; N &le; 10<sup>4</sup></li>
</ul>

<h3>Informações sobre a pontuação</h3>
<ul>
<li> Em um conjunto de casos de teste que totaliza 30 pontos, a primeira pessoa passa pelo sensor no tempo
0.</li>
</ul>

