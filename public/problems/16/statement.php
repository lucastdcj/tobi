<p>Certa vez, numa aula, a professora passou um filme para os alunos assistirem. Durante este filme, ela passou
uma lista de presença em sua sala para verificar a presença dos alunos, onde cada aluno deveria inserir apenas seu
número de registro. Alguns alunos contudo, como possuem amigos que fogem da aula, decidiram ser camaradas
e inseriram os números de registro de seus amigos fujões. O problema é que muitos alunos são amigos de alunos
que fogem da aula e alguns números de registro acabaram sendo repetidamente inseridos na lista de presença.
Além de tudo, alguns dos alunos que se esperava que não estivessem na aula de fato estavam!</p>
<p>A professora, ao notar que a lista de presença continha alguns números repetidos, ficou sem entender, mas
decidiu dar um voto de confiança e dar presença a todos os alunos cujos números de registro estavam na lista.
Como são muitos alunos na sala e muitos números com repetição, ela pediu a sua ajuda para determinar o total
de alunos que receberam presença na aula.</p>


<h3>Entrada</h3>
<p>A primeira linha da entrada contém um número inteiro <i>N</i> , que informa a quantidade de números de registro
que apareceram na lista de presença. Cada uma das <i>N</i> linhas seguintes contém um número de registro <i>V</i><sub>i</sub> 
que foi inserido na lista de presença.</p>

<h3>Sa&iacute;da</h3>
<p>Seu programa deve imprimir uma única linha, contendo apenas um número inteiro, o número de alunos que
receberam presença.</p>

<h3>Restri&ccedil;&otilde;es</h3>
<ul>
  <li>1 &le; <i>N</i> &le; 10<sup>5</sup></li>
  <li>Para cada elemento <i>V</i><sub>i</sub>, 0 &le; <i>V</i><sub>i</sub> &le; 10<sup>6</sup></li> 
</ul>

<h3>Informa&ccedil;&otilde;es sobre a pontua&ccedil;&atilde;o</h3>
<ul>
  <li>Em um conjunto de casos que totaliza 40 pontos, <i>N</i> &le; 10<sup>3</sup> e <i>V</i><sub>i</sub> &le; 10<sup>3</sup></li>  
</ul>

