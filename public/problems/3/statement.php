<p>
Por lei, na Nlogônia todas as barras de chocolate são quadradas. Anamaria tem uma barra quadrada de chocolate
de lado L, que ela quer compartilhar com alguns colegas da obi. Mas ela é uma boa cidadã e cumpre a lei.
Então, ela divide a barra em quatro pedaços quadrados, de lado L=L/2. Depois, ela repete esse procedimento com
cada pedaço gerado, sucessivamente, enquanto o lado for maior do que, ou igual a 2cm. Você deve escrever um
programa que, dado o lado L da barra inicial, em centímetros, determina quantos pedaços haverá ao final do
processo.</p>

<h3>Entrada</h3>
<p>
A entrada consiste de uma linha, com um único inteiro, L, 
o número de centímetros do lado do quadrado.</p>

<h3>Saída</h3>
<p>
Se programa deve imprimir uma única linha, contendo um único inteiro, igual ao número total de pedaços
obtidos pela Anamaria.
</p>


<h3>Restrições</h3>
<ul>
<li>2 &le; L &le; 10<sup>4</sup></li>
</ul>

<h3>Informações sobre a pontuação</h3>

<ul>
<li>Para um subconjunto dos casos de teste totalizando 30 pontos L < 64.</li>

</ul>

