<p>O governo do estado de Queensland est&aacute; com problemas s&eacute;rios de tr&acirc;nsito na capital Brisbane, onde est&atilde;o   os pr&eacute;dios administrativos. Para desafogar o tr&acirc;nsito, o prefeito de Brisbane e o governador de Queensland decidiram que uma nova capital administrativa deve ser constru&iacute;da em uma &aacute;rea fora de Brisbane. Para projetar a nova capital, o renomado arquiteto minimalista Joe Bloggs foi contratado.</p>

<p>Bloggs foi informado de que o terreno destinado &agrave; nova capital ainda n&atilde;o foi demarcado, mas ser&aacute; retangular. Al&eacute;m disso, a cidade dever&aacute; ser dividida em quatro zonas, uma delas destinada a uma reserva ambiental e cada uma das outras tr&ecirc;s receber&aacute; os novos pr&eacute;dios de cada um dos tr&ecirc;s poderes (Executivo, Legislativo e Judici&aacute;rio). Em um arroubo de criatividade, Bloggs decidiu que duas avenidas, perpendiculares entre si, cada uma paralela a dois dos lados do terreno retangular, dividir&atilde;o a capital nas quatro zonas.</p>

<p>Bloggs recebeu do governo as &aacute;reas de cada uma das zonas e, ap&oacute;s muito esfor&ccedil;o, encontrou um ret&acirc;ngulo que pode ser dividido conforme seus planos e de forma a respeitar as &aacute;reas delimitadas. No entanto, a Funda&ccedil;&atilde;o de Conserva&ccedil;&atilde;o dos Cangurus determinou que a &aacute;rea destinada &agrave; reserva ambiental era muito pequena, o que obrigou o governo a alterar as &aacute;reas das quatro zonas. Ap&oacute;s receber as novas medidas, Bloggs tentou encontrar um novo ret&acirc;ngulo que viabilizasse seu projeto, por&eacute;m sem sucesso. Cansado de fazer testes, ele pensou que talvez tenha que abandonar sua brilhante ideia. Por isso, ele pediu para voc&ecirc; escrever um programa que, dadas as &aacute;reas das quatro zonas, determine se ele poder&aacute; ou n&atilde;o manter seu projeto (ou seja, se existe um ret&acirc;ngulo que possa ser dividido por duas retas perpendiculares, cada uma paralela a dois dos lados do ret&acirc;ngulo, tal que as quatro &aacute;reas formadas obede&ccedil;am &agrave;s exig&ecirc;ncias do governo).</p>

<h3>Entrada</h3>

<p>A entrada consiste de uma &uacute;nica linha contendo quatro inteiros <em>A<sub>1</sub></em>, <em>A<sub>2</sub></em>, <em>A<sub>3</sub></em>, <em>A<sub>4</sub></em>, indicando a &aacute;rea de casa uma das zonas.</p>

<h3>Sa&iacute;da</h3>

<p>Imprima uma &uacute;nica linha contendo um &uacute;nico caractere: &lsquo;S&rsquo; se Bloggs pode preservar seu projeto e &lsquo;N&rsquo; caso contr&aacute;rio.</p>

