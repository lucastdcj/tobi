<p>Maria &eacute; uma moradora de Nlog&oacute;polis, uma cidade na Nlog&ocirc;nia que tem uma caracter&iacute;stica muito interessante: todas as ruas da cidade ou s&atilde;o orientadas no sentido norte-sul ou s&atilde;o orientadas no sentido leste-oeste. Isso significa que, dadas duas ruas, ou elas s&atilde;o paralelas ou elas s&atilde;o perpendiculares entre si.</p>

<p>Todas as ruas da cidade s&atilde;o de m&atilde;o dupla e &eacute; poss&iacute;vel seguir em qualquer dire&ccedil;&atilde;o em um cruzamento.</p>

<p>Agora Maria est&aacute; atrasada para uma reuni&atilde;o e precisa de sua ajuda. Dadas as coordenadas iniciais de Maria e da reuni&atilde;o, determine o n&uacute;mero m&iacute;nimo de cruzamentos que Maria deve atravessar para chegar ao seu destino. Esse n&uacute;mero inclui o cruzamento onde ocorrer&aacute; a reuni&atilde;o mas n&atilde;o inclui a posi&ccedil;&atilde;o inicial de Maria.</p>

<h3>Entrada</h3>

<p>A &uacute;nica linha da entrada cont&eacute;m quatro inteiros, <em>X<sub>m</sub></em>, <em>Y<sub>m</sub></em>, <em>X<sub>r</sub></em>, <em>Y<sub>r</sub></em>, indicando as coordenadas de Maria (<em>X<sub>m</sub></em>, <em>Y<sub>m</sub></em>) e da reuni&atilde;o (<em>X<sub>r</sub></em>, <em>Y<sub>r</sub></em>). O ponto de partida de Maria nunca ser&aacute; igual ao local da reuni&atilde;o, ou seja, pelo menos uma das coordenadas ser&aacute; diferente.</p>

<h3>Sa&iacute;da</h3>

<p>Seu programa deve imprimir uma &uacute;nica linha contendo um &uacute;nico inteiro: o n&uacute;mero m&iacute;nimo de cruzamentos que Maria precisa atravessar para chegar at&eacute; o local da reuni&atilde;o.</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>0 &le; <em>X<sub>m</sub></em>, <em>Y<sub>m</sub></em> &le; 1000 </li>

<li>0 &le; <em>X<sub>r</sub></em>, <em>Y<sub>r</sub></em> &le; 1000 </li>

</ul>

<h3>Exemplos</h3>

<pre><strong>Entrada</strong>

0 0 5 6



<strong>Sa&iacute;da</strong>

11



<strong>Entrada</strong>

52 75 120 75



<strong>Sa&iacute;da</strong>

68

</pre>
