<p align="justify">

<p>O Carnaval é um feriado celebrado normalmente em fevereiro; em muitas cidades brasileiras, a principal atração
são os desfiles de escolas de samba. As várias agremiações desfilam ao som de seus sambas-enredos e são julgadas
pela liga das escolas de samba para determinar a campeã do Carnaval.</p>
<p>Cada agremiação é avaliada em vários quesitos; em cada quesito, cada escola recebe cinco notas que variam de
5,0 a 10,0. A nota final da escola em um dado quesito é a soma das três notas centrais recebidas pela escola,
excluindo a maior e a menor das cinco notas.</p>
<p>Como existem muitas escolas de samba e muitos quesitos, o presidente da liga pediu que você escrevesse um
programa que, dadas as notas da agremiação, calcula a sua nota final num dado quesito.</p>

<h3>Entrada</h3>
<p>A entrada contém uma única linha, contendo cinco números <i>N</i><sub>i</sub> (1 &le; <i>i</i> &le; 5), todos com uma casa decimal,
indicando as notas recebidas pela agremiação em um dos quesitos.</p>

<h3>Sa&iacute;da</h3>
<p>Seu programa deve imprimir uma única linha, contendo um único número com exatamente uma casa decimal,
a nota final da escola de samba no quesito considerado.</p>

<h3>Restri&ccedil;&otilde;es</h3>
<ul>
  <li>5.0 &le; <i>N</i><sub>i</sub> &le; 10.0</li>
</ul>

