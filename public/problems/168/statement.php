<p>Renato gosta muito de geometria e acaba de achar alguns palitos em seu quarto. Ele est&aacute; tentando utilizar esses palitos de forma a fazer um pol&iacute;gono com o maior n&uacute;mero de lados poss&iacute;veis.</p>

<p>Para montar o pol&iacute;gono, Renato n&atilde;o quer cruzar os palitos; ou seja, os palitos devem se tocar apenas nas pontas. Ele tamb&eacute;m n&atilde;o quer quebrar nenhum palito, de forma que todos os palitos que forem usados devem manter sua medida original.</p>

<p>Se, por exemplo, os palitos t&ecirc;m medidas 3, 4 e 5, &eacute; poss&iacute;vel utilizar todos os tr&ecirc;s palitos para formar um tri&acirc;ngulo. Mas se as medidas s&atilde;o 1, 1, 1 e 5, &eacute; poss&iacute;vel formar um tri&acirc;ngulo com tr&ecirc;s lados iguais a 1 mas n&atilde;o &eacute; possivel formar um pol&iacute;gono com todos os 4 palitos.</p>

<p>Voc&ecirc; consegue ajudar Renato a descobrir qual &eacute; o maior n&uacute;mero de palitos que ele consegue usar?</p>

<h3>Entrada</h3>

<p>A primeira linha cont&eacute;m apenas um inteiro <em>N</em> que indica o n&uacute;mero de palitos. A segunda linha possui <em>N</em> inteiros indicando as medidas dos palitos.</p>

<h3>Sa&iacute;da</h3>

<p>Seu programa deve imprimir uma &uacute;nica linha, contendo um &uacute;nico inteiro, o maior n&uacute;mero de lados que o pol&iacute;gono pode ter seguindo as restri&ccedil;&otilde;es do enunciado. Se n&atilde;o for poss&iacute;vel formar nenhum pol&iacute;gono usando os palitos, imprima 0.</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>3 &le; <em>N</em> &le; 100000 </li>

<li>As medidas dos palitos s&atilde;o inteiros positivos menores ou iguais a 10000 </li>

</ul>

<h3>Exemplos</h3>

<pre><strong>Entrada</strong>

3

3 4 5



<strong>Sa&iacute;da</strong>

3



<strong>Entrada</strong>

3

3 1 2



<strong>Sa&iacute;da</strong>

0



<strong>Entrada</strong>

4

1 1 5 1



<strong>Sa&iacute;da</strong>

3

</pre>

