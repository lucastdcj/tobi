<p>A Guerra de Troia pode ter sido um grande conflito b&eacute;lico entre gregos e troianos, possivelmente ocorrido entre 1300 a.C. e 1200 a.C. (fim da Idade do Bronze no Mediterr&acirc;neo). Recentemente foram encontradas inscri&ccedil;&otilde;es numa caverna a respeito de sobreviventes. Ap&oacute;s um trabalho &aacute;rduo, arque&oacute;logos descobritam que as incri&ccedil;&otilde;es descreviam rela&ccedil;&otilde;es de parentesco numa certa popula&ccedil;&atilde;o. Cada item da inscri&ccedil;&atilde;o indicavam duas pessoas que pertenciam a uma mesma fam&iacute;lia. Seu problema &eacute; determinar quantas fam&iacute;lias distintas existem.</p>

<h3>Entrada</h3>

<p>O arquivo de entrada consiste de <em>M</em> + 1 linhas. A primeira linha do arquivo de entrada cont&eacute;m um inteiro positivo <em>N</em>, que indica o n&uacute;mero de elementos da comunidade, numerados de 1 a <em>N</em>. As demais <em>M</em> linhas do arquivo de entrada cont&ecirc;m, cada uma, dois inteiros. Cada inteiro identifica um elemento da comunidade. Cada linha indica que os dois indiv&iacute;duos pertencem a uma mesma fam&iacute;lia.</p>

<h3>Sa&iacute;da</h3>

<p>A sa&iacute;da deve conter apenas uma linha contendo um &uacute;nico inteiro, que &eacute; o n&uacute;mero de fam&iacute;lias.</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>1 &le; <em>N</em> &le; 5 &times; 10<sup>4</sup> </li>

<li>1 &le; <em>M</em> &le; 10<sup>5</sup> </li>

</ul>

