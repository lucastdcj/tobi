<p align="justify">

<p>Estamos no ano 2048 e um dos sonhos da humanidade torna-se finalmente realidade: a colonização do planeta
Marte. Nossos primeiros colonizadores acabam de chegar, e já começam a fazer as preparações (como a instalação
de cúpulas de oxigênio e tratamento do solo para agricultura) para que mais pessoas possam tentar uma nova
vida no planeta vizinho.</p>
<p>Apesar dos avanços tecnológicos e desafios vencidos, ainda resta um grande problema: os foguetes usados para
ir a Marte ainda são complicados e caros. Com isso, fica difícil enviar suprimentos para os nossos colonos
(enquanto a agricultura ainda não é possível) por muito tempo. Assim, a agência espacial contratou o SBC
(Serviço Balístico Cósmico), que desenvolveu um canhão super-potente que consegue disparar esferas até Marte,
sem precisar gastar milhões de dólares em equipamento e combustível.</p>
<p>Agora, tudo o que é necessário fazer para enviar suprimentos a Marte é colocar uma caixa com as encomendas
dentro de uma esfera e disparar a mesma até seu destino.</p>

<h3>Tarefa</h3>
<p>Dadas as dimensões de uma caixa com suprimentos e o raio interno da esfera que é disparada pelo canhão, seu
programa deverá dizer se á possível enviar tal caixa para Marte usando tal esfera.</p>

<h3>Entrada</h3>
<p>Cada entrada contém apenas uma linha com quatro inteiros <i>L</i>, <i>A</i>, <i>P</i> e <i>R</i>, (0 &le; <i>L</i>, <i>A</i>, <i>P</i>, <i>R</i> &le; 1000) que representam, respectivamente, a largura, altura e profundidade da caixa, e o raio da esfera.</p>

<h3>Sa&iacute;da</h3>
<p>Seu programa deve imprimir um único caractere: 'S' (sem aspas) se é possível colocar a caixa dentro da esfera,
ou 'N' (sem aspas) caso contrário.</p>

<h3>Informa&ccedil;&otilde;es sobre a pontua&ccedil;&atilde;o</h3>
<ul>
  <li>Em um conjunto de casos de teste que totaliza 20 pontos, 0 &le; <i>L</i>, <i>A</i>, <i>P</i>, <i>R</i> &le; 20.</li>
</ul>

