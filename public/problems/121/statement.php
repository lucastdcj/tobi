<p>Zezinho tem aulas de Iniciaç&#227;o Artística em sua escola, e recentemente aprendeu a fazer dobraduras

em papel. Ele ficou fascinado com as inúmeras possibilidades de se dobrar uma simples folha

de papel. Como Zezinho gosta muito de matemática, resolveu inventar um quebra-cabeça envolvendo

dobraduras. Zezinho definiu uma operaç&#227;o de dobradura D que consiste em dobrar duas

vezes uma folha de papel quadrada de forma a conseguir um quadrado com 1/4 do tamanho original,

conforme ilustrado na figura.



<p><img src="https://www.spoj.pl/content/wanderley:dobra1.jpg"/>



<p>Depois de repetir N vezes esta operaç&#227;o de dobradura D sobre o papel, Zezinho cortou o quadrado

resultante com um corte vertical e um corte horizontal, conforme a figura abaixo.



<p><img src="https://www.spoj.pl/content/wanderley:dobra2.jpg"/>



<p>Zezinho lançou ent&#227;o um desafio aos seus colegas: quem adivinha quantos pedaços de papel

foram produzidos?



<h3>Entrada</h3>

<p>A entrada é composta de vários conjuntos de teste. Cada conjunto de teste é composto de uma

única linha, contendo um número inteiro N que indica o número de vezes que a operaç&#227;o de

dobradura D foi aplicada. O final da entrada é indicado por N = -1.



<h3>Saída</h3>

<p>Para cada conjunto de teste da entrada seu programa deve produzir tr&#234;s linhas na saída. A primeira

linha deve conter um identificador do conjunto de teste, no formato &#8220;Teste n&#8221;, onde n é numerado a partir de 1. A segunda linha deve conter o número de pedaços de papel obtidos depois de cortar

a dobradura, calculado pelo seu programa. A terceira linha deve ser deixada em branco. A grafia

mostrada no Exemplo de Saída, abaixo, deve ser seguida rigorosamente.



<h3>Exemplo</h3>



<pre>

<b>Entrada:</b>

1

0

-1



<b>Saída:</b>

Teste 1

9



Teste 2

4

</pre>



<h3>Restriç&#245;es</h3>

<p>-1 <= N <= 15 (N = -1 apenas para indicar o fim da entrada)


