<p>A Otac&iacute;lio Bus&iacute;lis Im&oacute;veis (OBI) &eacute; a maior imobili&aacute;ria de Nlog&oacute;polis, especializada no aluguel de pr&eacute;dios comerciais; todas as suas propriedades se localizam na Avenida Doutor Otac&iacute;lio Bus&iacute;lis, assim chamada em homenagem ao fundador da OBI.</p>

<p>Devido &agrave; crise econ&ocirc;mica mundial, a OBI precisa vender <em>K</em> de seus im&oacute;veis para levantar capital de giro. Dr. Otac&iacute;lio quer que os pr&eacute;dios restantes ap&oacute;s a venda sejam o mais pr&oacute;ximos poss&iacute;vel &mdash; ou seja, a dist&acirc;ncia entre o primeiro e o &uacute;ltimo pr&eacute;dios restantes deve ser a menor poss&iacute;vel.</p>

<p>Infelizmente, a OBI &eacute; propriet&aacute;ria de tantos pr&eacute;dios que o Dr. Otac&iacute;lio n&atilde;o sabe quais pr&eacute;dios ele deve vender; ele lhe contratou para que voc&ecirc; escreva um programa que determina qual &eacute; a m&iacute;nima dist&acirc;ncia poss&iacute;vel entre o primeiro e o &uacute;ltimo pr&eacute;dios da OBI na avenida, ap&oacute;s a venda de <em>K</em> pr&eacute;dios.</p>

<h3>Entrada</h3>

<p>A primeira linha da entrada cont&eacute;m os inteiros <em>N</em> e <em>K</em>, indicando, respectivamente, quantos pr&eacute;dios a OBI possui, e quantos pr&eacute;dios ela pretende vender. A linha seguinte cont&eacute;m <em>N</em> inteiros <em>X<sub>i</sub></em>, indicando a dist&acirc;ncia de cada um dos <em>N</em> pr&eacute;dios ao in&iacute;cio da avenida, em metros.</p>

<h3>Sa&iacute;da</h3>

<p>A sa&iacute;da deve conter um &uacute;nico inteiro indicando a menor dist&acirc;ncia poss&iacute;vel entre o primeiro e o &uacute;ltimo pr&eacute;dio possu&iacute;dos pela OBI ap&oacute;s a venda.</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>3 &le; N &le; 10<sup>5</sup> </li>

<li><em>N</em> - K &ge; 2 </li>

<li>1 &le; <em>X<sub>i</sub></em> &le; 10<sup>6</sup> </li>

<li>todos os <em>X<sub>i</sub></em> s&atilde;o distintos </li>

</ul>

<h3>Exemplos</h3>

<pre><strong>Entrada</strong>

5 2

10 7 4 8 2



<strong>Sa&iacute;da</strong>

3



<strong>Entrada</strong>

8 6

16 11 1 7 29 4 22 2



<strong>Sa&iacute;da</strong>

1

