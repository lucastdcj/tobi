<p>Vov&ocirc; Jo&atilde;o tem uma banca de jornais; ele tem muitos clientes, e diariamente recebe muito dinheiro,   mas tamb&eacute;m faz muitos pagamentos para manter o seu estoque de jornais e revistas. Todo dia ele   vai ao banco realizar um dep&oacute;sito ou uma retirada de dinheiro. Em alguns dias, o saldo de sua conta   no banco fica negativo, mas Vov&ocirc; Jo&atilde;o tem um acordo com o banco que garante que ele somente &eacute;   cobrado se o saldo for menor do que um valor pr&eacute;-estabelecido.</p>

<p>Dada a movimenta&ccedil;&atilde;o di&aacute;ria da conta do banco do Vov&ocirc; Jo&atilde;o, voc&ecirc; deve escrever um programa que   calcule o menor saldo da conta, no per&iacute;odo dado.</p>

<h3>Entrada</h3>

<p>A primeira linha da entrada cont&eacute;m dois n&uacute;meros inteiros N e S que indicam respectivamente o   n&uacute;mero de dias do per&iacute;odo de interesse e o saldo da conta no in&iacute;cio do per&iacute;odo. Cada uma das N   linhas seguintes cont&eacute;m um n&uacute;mero inteiro indicando a movimenta&ccedil;&atilde;o de um dia (valor positivo no   caso de dep&oacute;sito, valor negativo no caso de retirada). A movimenta&ccedil;&atilde;o &eacute; dada para um per&iacute;odo de   N dias consecutivos: a primeira das N linhas corresponde ao primeiro dia do per&iacute;odo de interesse,   a segunda linha corresponde ao segundo dia, e assim por diante.</p>

<h3>Sa&iacute;da</h3>

<p>Seu programa deve imprimir uma &uacute;nica linha, contendo um &uacute;nico n&uacute;mero inteiro, o menor valor de   saldo da conta no per&iacute;odo dado.</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>1 &le; <em>N</em> &le; 30 </li>

<li>-10<sup>3</sup> &le; <em>S</em> &le; 10<sup>3</sup> </li>

<li>-10<sup>3</sup> &le; cada movimenta&ccedil;&atilde;o &le; 10<sup>3</sup> </li>

</ul>

