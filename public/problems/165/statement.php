<p>A Olimp&iacute;ada Internacional de Inform&aacute;tica (IOI, no original em ingl&ecirc;s) &eacute; a mais prestigiada competi&ccedil;&atilde;o de programa&ccedil;&atilde;o para alunos de ensino m&eacute;dio; seus aproximadamente 300 competidores se re&uacute;nem em um pa&iacute;s diferente todo ano para os dois dias de prova da competi&ccedil;&atilde;o. Naturalmente, os competidores usam o tempo livre para acessar a Internet, programar e jogar em seus notebooks, mas eles se depararam com um problema: o sagu&atilde;o do hotel s&oacute; tem uma tomada.</p>

<p>Felizmente, os quatro competidores da equipe brasileira da IOI trouxeram cada um uma r&eacute;gua de tomadas, permitindo assim ligar v&aacute;rios notebooks em uma tomada s&oacute;; eles tamb&eacute;m podem ligar uma r&eacute;gua em outra para aumentar ainda mais o n&uacute;mero de tomadas dispon&iacute;veis. No entanto, como as r&eacute;guas t&ecirc;m muitas tomadas, eles pediram para voc&ecirc; escrever um programa que, dado o n&uacute;mero de tomadas em cada r&eacute;gua, determina quantas tomadas podem ser disponibilizadas no sagu&atilde;o do hotel.</p>

<h3>Entrada</h3>

<p>A entrada consiste de uma linha com quatro inteiros positivos <em>T<sub>1</sub></em>, <em>T<sub>2</sub></em>, <em>T<sub>3</sub></em>, <em>T<sub>4</sub></em>, indicando o n&uacute;mero de tomadas de cada uma das quatro r&eacute;guas.</p>

<h3>Sa&iacute;da</h3>

<p>Seu programa deve imprimir uma &uacute;nica linha contendo um &uacute;nico n&uacute;mero inteiro, indicando o n&uacute;mero m&aacute;ximo de notebooks que podem ser conectados num mesmo instante.</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>2 &le; <em>T<sub>i</sub></em> &le; 6 </li>

</ul>

<h3>Exemplos</h3>

<pre><strong>Entrada</strong>

2 4 3 2



<strong>Sa&iacute;da</strong>

8



<strong>Entrada</strong>

6 6 6 6



<strong>Sa&iacute;da</strong>

21



<strong>Entrada</strong>

2 2 2 2



<strong>Sa&iacute;da</strong>

5

</pre>

