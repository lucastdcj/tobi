<p align="justify">

Senhor Coelho e conhecido mundialmente pela fabricacao de quadrados magicos de dimensoes 3 x 3. Um
quadrado e chamado magico quando a soma dos elementos de uma determinada linha, coluna ou diagonal e
sempre igual.
Infelizmente, assaltantes invadiram recentemente a oficina do Sr. Coelho e roubaram alguns dos numeros
de seus quadrados magicos. Felizmente os meliantes nao conseguiram roubar mais do que 3 numeros de cada
quadrado. Desesperado, pois devia entregar os quadrados naquele dia, o Sr. Coelho veio procurar a sua ajuda
para tentar completar os quadrados com os numeros faltantes.

<h3> Tarefa </h3> 
Escreva um programa que, dado um quadrado magico com alguns numeros faltando, determine qual era o
quadrado magico original.

<h3> Entrada </h3>
A entrada contem um unico conjunto de testes, que deve ser lido do dispositivo de entrada padrao (normalmente
o teclado). A entrada contem tres linhas, cada uma contendo tres inteiros N (0 <= N <= 20000). O numero zero
representa os digitos que foram roubados. Existem no maximo tres numeros zero na entrada.

<h3> Saida </h3>
Seu programa deve imprimir, na saida padrao, tres linhas, cada uma contendo tres inteiros, descrevendo a
configuracao original do quadrado magico.

<h3> Exemplos: </h3>
<pre>
Entrada
4 9 2
3 0 7
8 1 6

Saída
4 9 2
3 5 7
8 1 6

Entrada
0 12 12
16 10 0
8 8 14

Saída
6 12 12
16 10 4
8 8 14

Entrada
495 468 0
0 522 414
441 0 549

Saída
495 468 603
630 522 414
441 576 549
</pre>

