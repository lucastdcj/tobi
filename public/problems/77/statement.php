<p>Rosy é uma talentosa professora do Ensino Médio que já ganhou muitos prêmios pela qualidade de sua aula.
Seu reconhecimento foi tamanho que foi convidada a dar aulas em uma escola da Inglaterra. Mesmo falando
bem inglês, Rosy ficou um pouco apreensiva com a responsabilidade, mas resolveu aceitar a proposta e encará-la
como um bom desafio.</p>
<p>Tudo ocorreu bem para Rosy até o dia da prova. Acostumada a dar notas de 0 (zero) a 100 (cem), ela fez o
mesmo na primeira prova dos alunos da Inglaterra. No entanto, os alunos acharam estranho, pois na Inglaterra
o sistema de notas é diferente: as notas devem ser dadas como conceitos de A a E. O conceito A é o mais alto,
enquanto o conceito E é o mais baixo.</p>
<p>Conversando com outros professores, ela recebeu a sugestão de utilizar a seguinte tabela, relacionando as notas
numéricas com as notas de conceitos:</p>
<center> 
  <img src="task_images/2009f1p2_nota.png" width="210px" height="170px">
</center>
<p>O problema é que Rosy já deu as notas no sistema numérico, e terá que converter as notas para o sistema de
letras. Porém, Rosy precisa preparar as próximas aulas (para manter a qualidade que a tornou reconhecida), e
não tem tempo suficiente para fazer a conversão das notas manualmente.</p>

<h3>Tarefa</h3>
<p>Você deve escrever um programa que recebe uma nota no sistema numérico e determina o conceito correspondente.</p>

<h3>Entrada</h3>
<p>A entrada contém um único conjunto de testes, que deve ser lido do <i>dispositivo de entrada padrão</i> (normalmente
o teclado). A entrada contém uma única linha com um número inteiro <i>N</i> (0 &le; <i>N</i> &le; 100), representando uma
nota de prova no sistema numérico.</p>

<h3>Sa&iacute;da</h3>
<p>Seu programa deve imprimir, na <i>saída padrão</i>, uma letra (A, B, C, D, ou E em maiúsculas) representando o
conceito correspondente à nota dada na entrada.</p>

<h3>Informa&ccedil;&otilde;es sobre a pontua&ccedil;&atilde;o</h3>
<ul>
  <li>Em um conjunto de casos de teste que totaliza 35 pontos, <i><i>N</i></i> &le; 10.</li>
  <li>Em um conjunto de casos de teste que totaliza 70 pontos, <i><i>N</i></i> &le; 50.</li>  
</ul>


