<p align="justify">

<p>João está comprando móveis novos para sua casa. Agora é a vez de comprar um colchão novo, de molas, para
substituir o colchão velho. As portas de sua casa têm altura <i>H</i> e largura <i>L</i> e existe um colchão que está em
promoção com dimensões <i>A</i> x <i>B</i> x <i>C</i>.</p>
<p>O colchão tem a forma de um paralelepípedo reto retângulo e João só consegue arrastá-lo através de uma porta
com uma de suas faces paralelas ao chão, mas consegue virar e rotacionar o colchão antes de passar pela porta.</p>
<p>Entretanto, de nada adianta ele comprar o colchão se ele não passar através das portas de sua casa. Portanto
ele quer saber se consegue passar o colchão pelas portas e para isso precisa de sua ajuda.</p>


<h3>Entrada</h3>
<p>A primeira linha da entrada contém três números inteiros <i>A</i>, <i>B</i> e <i>C</i>, as três dimensões do colchão, em centímetros.
A segunda linha contém dois inteiros <i>H</i> e <i>L</i>, respectivamente a altura e a largura das portas em centímetros.</p>

<h3>Sa&iacute;da</h3>
<p>Se programa deve escrever uma única linha, contendo apenas a letra `S` se o colchão passa pelas portas e apenas
a letra `N` em caso contrário.</p>


<h3>Restri&ccedil;&otilde;es</h3>
<ul>
  <li>1 &le; <i>A<i>, <i>B<i>, <i>C<i> &le; 300</i></i></i></i></i></i></li>
  <li>1 &le; <i>H<i>, <i>L<i> &le; 250</i></i></i></i></li>   
</ul>

