<p align="justify">

<p>A OBI (Organização de Bocha Internacional) é responsável por organizar a competição mundial de bocha.
Infelizmente esse esporte não é muito popular, e numa tentativa de aumentar a sua popularidade, ficou decidido
que seriam chamados, para a Grande Final Mundial, o campeão e o vice-campeão de cada sede nacional, ao
invés de apenas o primeiro lugar.</p>
<p>Tumbólia é um país pequeno que já havia realizado a sua competição nacional quando a nova regra foi instituída,
e o comitê local não armazenou quem foi o segundo classificado. Felizmente eles armazenaram a pontuação de
todos competidores - que foram apenas três, devido ao tamanho diminuto do país. Sabe-se também que as
pontuações de todos jogadores foram diferentes, de forma que não ocorreu empate entre nenhum deles.</p>
<p>Resta agora descobrir quem foi o vice-campeão e para isso o comitê precisa de ajuda.</p>

<h3>Entrada</h3>
<p>A primeira e única linha da entrada consiste de três inteiros separados por espaços, <i>A</i>, <i>B</i> e <i>C</i>, as pontuações
dos 3 competidores.</p>

<h3>Sa&iacute;da</h3>
<p>Imprima uma única linha na saída, contendo apenas um número inteiro, a pontuação do vice-campeão.</p>

<h3>Restri&ccedil;&otilde;es</h3>
<ul>
  <li>1 &le; <i>A</i> &le; 100</li>
  <li>1 &le; <i>B</i> &le; 100</li>
  <li>1 &le; <i>C</i> &le; 100</li>
</ul>

