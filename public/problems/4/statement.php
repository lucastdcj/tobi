<p>
Leonardo é um garoto muito criativo. Ele adora criar desa?os para seus colegas da escola. Seu último desa?o é
o seguinte: diversos números são ditos em voz alta, quando o número 0 (zero) é dito então o desa?o termina e
seus colegas devem dizer imediatamente qual foi o maior número. Leonardo tem muita di?culdade de veri?car
se a resposta dada pelos colegas é correta ou não, pois a sequência de números costuma ser longa. Por este
motivo, ele resolveu pedir sua ajuda.</p>
<p>
Sua tarefa é escrever um programa que dada uma sequência de números inteiros positivos terminada por 0
(zero), imprime o maior número da sequência.</p>

<h3>Entrada</h3>
<p>
A entrada é dada em uma única linha contendo uma sequência de números inteiros positivos. O último número
da linha é 0 (zero).</p>
<h3>Saída</h3>
<p>
Seu programa deve imprimir o maior número dentre os números da entrada.
</p>


<h3>Restrições</h3>
<ul>
<li>1 &le; tamanho da sequência  &le; 100</li>
<li>1 &le; número da sequência  &le; 100</li>
</ul>

