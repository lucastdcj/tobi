<p align="justify">

<p>Leonardo é um corredor profissional que participa de diversos campeonatos de atletismo pelo mundo. O tamanho
das pistas ao redor do mundo não é padronizado. Por isso, Leonardo, que treina em um clube que possui uma
pista circular, resolveu fixar seu treinamento em <i>C</i> metros, ao invés de um número fixo de voltas na pista. Após
cada treinamento, Leonardo deve tomar meio litro de água antes de fazer qualquer esforço, e por isso quer deixar
sua garrafa de água exatamente no ponto da pista onde ele termina o seu treinamento.</p>
<p>Sabendo o comprimento da pista de corrida que Leonardo pretende treinar, ele resolveu pedir sua ajuda para
calcular o local do <i>ponto de término</i> do treinamento. O ponto de término é o local da pista onde ele termina o
percurso de <i>C</i> metros considerando que ele parte do ponto de partida e se movimenta sempre na mesma direção.
O ponto de término é dado pelo número de metros entre o ponto de partida e o local onde Leonardo termina
seu treinamento, contados na direção do percurso. Leonardo quer deixar sua garrafa de água neste ponto.</p>
<p>Por exemplo, se a pista tem 12 metros e Leonardo fixou seu treinamento em 22 metros, o ponto de término é
10.</p>
<p>Sua tarefa é, dado o número <i>C</i> de metros que Leonardo pretende correr e o comprimento <i>N</i> em metros da pista
circular, determinar o ponto de término de seu treinamento.</p>

<h3>Entrada</h3>
<p>A entrada consiste em apenas uma linha contendo dois inteiros <i>C</i> e <i>N</i> que indicam, respectivamente, o número
de metros que Leonardo pretende correr e o comprimento da pista.</p>

<h3>Sa&iacute;da</h3>
<p>Seu programa deve imprimir apenas uma linha, contendo apenas um inteiro, indicando o ponto de término do
treinamento de Leonardo.</p>

<h3>Restri&ccedil;&otilde;es</h3>
<ul>
  <li>1 &le; <i>C</i> &le; 10<sup>8</sup></li>
  <li>1 &le; <i>N</i> &le; 100</li> 
</ul>

