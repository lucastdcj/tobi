<p>Voc&ecirc; est&aacute; num supermercado e est&aacute; na fila do caixa para comprar alguns produtos. Assim que voc&ecirc; termina de passar as compras pelo caixa, se lembra que tem v&aacute;rias moedas em seu bolso, algumas repetidas, e fica pensando se com elas d&aacute; para pagar exatamente o valor das compras (para assim se livrar destas moedas e ficar com os bolsos mais leves). Voc&ecirc; consegue pagar o valor exato da conta usando estas moedas?</p>

<h3>Entrada</h3>

<p>A primeira linha da entrada cont&eacute;m dois n&uacute;meros inteiros <em>V</em> e <em>M</em>, indicando, respectivamente, o valor final da compra e o n&uacute;mero de moedas que voc&ecirc; tem em seu bolso. A segunda linha cont&eacute;m <em>M</em> n&uacute;meros inteiros que descrevem o valor <em>M<sub>i</sub></em> de cada moeda existente em seu bolso.</p>

<h3>Sa&iacute;da</h3>

<p>Seu programa deve imprimir apenas uma linha, contendo apenas um caractere: <code>S</code> caso seja poss&iacute;vel pagar o valor exato da conta usando apenas suas moedas, ou <code>N</code> caso contr&aacute;rio.</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>1 &le; <em>V</em> &le; 10<sup>5</sup> </li>

<li>1 &le; <em>M</em> &le; 10<sup>3</sup> </li>

<li>1 &le; <em>M<sub>i</sub></em> &le; 10<sup>5</sup> </li>

</ul>

<h3>Exemplos</h3>

<pre><strong>Entrada</strong>

16 4

25 10 5 1



<strong>Sa&iacute;da</strong>

S



<strong>Entrada</strong>

20 4

25 10 5 1



<strong>Sa&iacute;da</strong>

N

</pre>

