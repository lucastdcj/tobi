<p>Assim, por exemplo, se o consumo foi de 120 m<sup>3</sup>, o valor da conta é:</p>
<ul>
<li>7 reais da assinatura básica;</li>
<li>20 reais pelo consumo no intervalo 11 - 30 m<sup>3</sup>;</li>
<li>140 reais pelo consumo no intervalo 31 - 100 m<sup>3</sup>;</li>
<li>100 reais pelo consumo no intervalo 101 - 120 m<sup>3</sup>.</li>
</ul>
<p>Logo o valor total da conta de água é R$ 267.</p>

<h3>Tarefa</h3>
<p>Escreva um programa que, dado o consumo de uma residência em m<sup>3</sup>, calcula o valor da conta de água daquela
residência.</p>

<h3>Entrada</h3>
<p>A única linha da entrada contém um único inteiro <i>N</i>, indicando o consumo de água da residência, em m<sup>3</sup>
(0 &le; <i>N</i> &le; 103).</p>

<h3>Sa&iacute;da</h3>
<p>Seu programa deve imprimir uma única linha, contendo o valor da conta de água daquela residência.</p>

