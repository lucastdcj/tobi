<p>A empresa local de abastecimento de água, a Saneamento Básico da Cidade (SBC), está promovendo uma
campanha de conservaçãoo de água, distribuindo cartilhas e promovendo açãoes demonstrando a importância da
água para a vida e para o meio ambiente.</p>
<p>Para incentivar mais ainda a economia de água, a SBC alterou os preços de seu fornecimento de forma que,
proporcionalmente, aqueles clientes que consumirem menos água paguem menos pelo metro cúbico. Todo cliente
paga mensalmente uma assinatura de R$ 7, que inclui uma franquia de 10 m<sup>3</sup> de água. Isto é, para qualquer
consumo entre 0 e 10 m<sup>3</sup>, o consumidor paga a mesma quantia de R$ 7 reais (note que o valor da assinatura deve
ser pago mesmo que o consumidor não tenha consumido água). Acima de 10 m<sup>3</sup>, cada metro cúbico subsequente
tem um valor diferente, dependendo da faixa de consumo. A SBC cobra apenas por quantidades inteiras de
metros cúbicos consumidos. A tabela abaixo especifica o preço por metro cúbico para cada faixa de consumo:</p>

  
