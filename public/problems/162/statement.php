<p>Jo&atilde;ozinho est&aacute; aprendendo a somar fra&ccedil;&otilde;es na escola e quer sua ajuda para escrever um programa que dadas duas fra&ccedil;&otilde;es imprima a soma delas em sua forma irredut&iacute;vel. Assim ele vai poder conferir as respostas dos exerc&iacute;cios que est&aacute; fazendo.</p>

<p>A forma irredut&iacute;vel de uma fra&ccedil;&atilde;o &eacute; quando o divisor (n&uacute;mero de baixo) &eacute; o menor poss&iacute;vel. Por exemplo, <sup>10</sup>&frasl;<sub>3</sub> &eacute; uma fra&ccedil;&atilde;o irredut&iacute;vel, pois 10 e 3 n&atilde;o t&ecirc;m nenhum divisor em comum. Mas <sup>10</sup>&frasl;<sub>6</sub> n&atilde;o &eacute;, pois ela pode ser simplificada para <sup>5</sup>&frasl;<sub>3</sub>, dividindo-se 10 e 6 por 2.</p>

<p>Dados quatro inteiros <em>a</em>, <em>b</em>, <em>c</em>, <em>d</em>, escreva um programa que calcule <sup>a</sup>&frasl;<sub>b</sub> + <sup>c</sup>&frasl;<sub>d</sub> na sua forma irredut&iacute;vel.</p>

<h3>Entrada</h3>

<p>A &uacute;nica linha da entrada cont&eacute;m quatro inteiros <em>a</em>, <em>b</em>, <em>c</em>, <em>d</em>, respectivamente dividendo e divisor da primeira   fra&ccedil;&atilde;o e dividendo e divisor da segunda fra&ccedil;&atilde;o.</p>

<h3>Sa&iacute;da</h3>

<p>Seu programa deve imprimir uma &uacute;nica linha contendo dois inteiros, dividendo e divisor da fra&ccedil;&atilde;o irredut&iacute;vel formada pela soma das duas fra&ccedil;&otilde;es dadas.</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>1 &le; <em>a</em>, <em>b</em>, <em>c</em>, <em>d</em> &le; 100 </li>

</ul>

