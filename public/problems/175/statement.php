<p>A sala de aulas utilizada para os cursos da OBI tem uma grande janela, composta de tr&ecirc;s folhas de vidro. A janela tem um metro de altura por seis metros de comprimento. Cada folha da janela tem um metro de altura e dois metros de comprimento. As folhas deslizam sobre trilhos, ao longo do comprimento da janela, de forma que &eacute; poss&iacute;vel controlar a abertura da janela, para circula&ccedil;&atilde;o de ar.</p>

<p>Dadas as posi&ccedil;&otilde;es das tr&ecirc;s folhas da janela, deseja-se determinar qual a &aacute;rea da janela que est&aacute; aberta, em cent&iacute;metros quadrados.</p>

<p>A figura abaixo ilustra duas configura&ccedil;&otilde;es das folhas da janela. Na figura, os cantos inferiores esquerdos de cada folha s&atilde;o indicados por F1, F2 e F3. Na configura&ccedil;&atilde;o (a) a janela est&aacute; totalmente fechada, e portanto o total da &aacute;rea aberta &eacute; igual a zero. Na configura&ccedil;&atilde;o (b) h&aacute; duas aberturas, e o total de &aacute;rea aberta &eacute; igual a (100 &times; 100) + (50 &times; 100) = 15.000 <em>cm</em><sup>2</sup>.</p>

<img src="http://www.spoj.com/content/marcoskwkm:janela13.jpg" alt="" />

<p>Dadas as posi&ccedil;&otilde;es das tr&ecirc;s folhas da janela, escreva um programa que calcule a &aacute;rea da janela que est&aacute; aberta, em cent&iacute;metros quadrados.</p>

<h3>Entrada</h3>

<p>A primeira e &uacute;nica linha da entrada cont&eacute;m tr&ecirc;s inteiros <em>F<sub>1</sub></em>, <em>F<sub>2</sub></em> e <em>F<sub>3</sub></em>, indicando as posi&ccedil;&otilde;es das tr&ecirc;s folhas. A posi&ccedil;&atilde;o de cada folha &eacute; dada pela dist&acirc;ncia, em cent&iacute;metros, da extremidade esquerda da janela at&eacute; a extremidade esquerda da folha.</p>

<h3>Sa&iacute;da</h3>

<p>Seu programa deve imprimir uma &uacute;nica linha, contendo um &uacute;nico inteiro, a &aacute;rea aberta da janela em cent&iacute;metros quadrados.</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>0 &le; <em>F<sub>1</sub></em>, <em>F<sub>2</sub></em>, <em>F<sub>3</sub></em> &le; 400 </li>

</ul>

<h3>Exemplos</h3>

<pre><strong>Entrada</strong>

0 200 400



<strong>Sa&iacute;da</strong>

0



<strong>Entrada</strong>

0 50 350



<strong>Sa&iacute;da</strong>

15000



<strong>Entrada</strong>

344 344 344



<strong>Sa&iacute;da</strong>

40000

</pre>
