Uma construtora, durante a criacao de um parque tematico, encontrou no terreno um conjunto de varios pilhas
de cubos de pedra. Ao inves de pagar pela remocao dos cubos de pedras, um dos arquitetos da empresa achou
interessante utilizar as pedras para decoracao do parque, determinando que as pedras fossem rearranjadas no
formato de "escada". Para isso, os funcionarios deveriam mover alguns cubos para formar os degraus das
escadas. So que o arquiteto decidiu que, entre uma pilha e outra de pedras deveria haver exatamente uma pedra
de diferenca, formando o que ele chamou de escada perfeita. O exemplo abaixo mostra um conjunto de cinco
pilhas de pedras encontradas e as cinco pilhas como ficaram apos a arrumacao em escada perfeita.

<h3> Tarefa </h3>
Dada uma sequencia de pilhas de cubos de pedras com suas respectivas alturas, voce deve determinar o numero
minimo de pedras que precisam ser movidas para formar uma escada perfeita com exatamente o mesmo numero
de pilhas de pedras encontrado inicialmente (ou seja, nao devem ser criadas ou eliminadas pilhas de pedras). O
degrau mais baixo da escada deve sempre estar do lado esquerdo.

<h3> Entrada </h3>
A entrada contem um unico conjunto de testes, que deve ser lido do dispositivo de entrada padrao (normalmente
o teclado). A primeira linha contem um inteiro N que indica o numero de pilhas de pedras. A segunda linha
contem N numeros inteiros que indicam a quantidade de cubos de pedras em cada uma das pilhas, da esquerda
para a direita.

<h3> Saida </h3>
Seu programa deve imprimir, na saida padrao, uma unica linha, contendo um inteiro: o numero minimo de
cubos de pedras que devem ser movidos para transformar o conjunto de pilhas em uma escada perfeita, conforme
calculado pelo seu programa. Caso nao seja possivel efetuar a transformacao em escada perfeita, imprima como
resultado o valor -1.

<h3> Exemplos </h3>
<pre>
Entrada
5
5 4 5 4 2

Saída
5

Entrada
6
9 8 7 6 5 4

Saída
9

Entrada
2
1 5

Saída
-1

</pre>

