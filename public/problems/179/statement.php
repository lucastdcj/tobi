<p>Jo&atilde;o disp&otilde;e de dois peda&ccedil;os retangulares de tecido, e quer us&aacute;-los para fazer um len&ccedil;ol, tamb&eacute;m retangular, de dimens&otilde;es A &times; B. Se necess&aacute;rio, os dois peda&ccedil;os retangulares podem ser unidos por uma costura, mas Jo&atilde;o quer que a costura seja paralela aos lados dos ret&acirc;ngulos. Os cortes, se necess&aacute;rios, tamb&eacute;m devem ser paralelos aos lados dos ret&acirc;ngulos.</p>

<p>Dadas as dimens&otilde;es dos peda&ccedil;os de tecido e do len&ccedil;ol, escreva um programa que determina se &eacute; poss&iacute;vel Jo&atilde;o fazer o len&ccedil;ol com as dimens&otilde;es desejadas.</p>

<h3>Entrada</h3>

<p>A entrada cont&eacute;m uma &uacute;nica linha, com seis inteiros <em>A<sub>1</sub></em>, <em>B<sub>1</sub></em>, <em>A<sub>2</sub></em>, <em>B<sub>2</sub></em>, <em>A</em> e <em>B</em>, representando, respectivamente, as dimens&otilde;es dos dois ret&acirc;ngulos dispon&iacute;veis, e as dimens&otilde;es do ret&acirc;ngulo desejado.</p>

<h3>Sa&iacute;da</h3>

<p>Seu programa deve imprimir uma &uacute;nica linha contendo um caractere S se &eacute; poss&iacute;vel fazer o len&ccedil;ol, e N caso contr&aacute;rio.</p>

<h3>Restri&ccedil;&otilde;es</h3>

<ul>

<li>1 &le; <em>A<sub>1</sub></em>, <em>B<sub>1</sub></em>, <em>A<sub>2</sub></em>, <em>B<sub>2</sub></em>, <em>A</em>, <em>B</em> &le; 10<sup>6</sup> </li>

</ul>

